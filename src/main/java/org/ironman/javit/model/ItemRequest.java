package org.ironman.javit.model;

/**
 * ItemRequest
 */
public class ItemRequest {

    private String title;
    private Integer qty;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Integer getQty() {
        return qty;
    }

    public void setQty(Integer qty) {
        this.qty = qty;
    }

    
}